#include "libft.h"

void	ft_putnbr(int nb)
{
	long l;

	l = (long)nb;
	if (l < 0)
	{
		ft_putchar('-');
		l = l * -1;
	}
	if (l <= 9)
		ft_putchar(l + '0');
	else
	{
		ft_putnbr(l / 10);
		ft_putchar(l % 10 + '0');
	}
}
