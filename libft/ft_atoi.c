#include "libft.h"

int		ft_atoi(const char *s)
{
	size_t	i;
	size_t	j;
	int		out;

	i = 0;
	j = 0;
	out = 0;
	while (s[i] == 32 || (s[i] >= 9 && s[i] <= 13))
		i++;
	j = i;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		out = out * 10 + s[i] - '0';
		i++;
	}
	if (s[j] == '-')
		out = out * -1;
	return (out);
}
